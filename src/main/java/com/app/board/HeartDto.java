package com.app.board;

public class HeartDto {

	private int heart_number;
	private int board_number;
	private int heart_cnt;
	
	public int getHeart_number() {
		return heart_number;
	}
	public void setHeart_number(int heart_number) {
		this.heart_number = heart_number;
	}
	public int getBoard_number() {
		return board_number;
	}
	public void setBoard_number(int board_number) {
		this.board_number = board_number;
	}
	public int getHeart_cnt() {
		return heart_cnt;
	}
	public void setHeart_cnt(int heart_cnt) {
		this.heart_cnt = heart_cnt;
	}
}
